public class IWontCheatOnTheExams {
    public static void main(String[] args) {
        // Create a program that writes this line 100 times:
        // "I won't cheat on the exam!"
        int counter = 0;
        while (counter<100)
        {
            counter++;
            System.out.println(counter + " - I won't cheat on the exam!");
        }
    }
}
